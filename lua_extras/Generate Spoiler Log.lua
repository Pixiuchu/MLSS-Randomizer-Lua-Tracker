local function getRomItems()
	file = io.open("spoilerlog.txt", "a")
	local i = 1
	while i < 601 do -- 521 currently
		local item
		local itemText = ""
		local doPrint = ""
		local address = romTable[i][1]
		local locationType = romTable[i][2]
		local location = romTable[i][3]
		
		if locationType == 1 then
			item = memory.read_u16_be(address, "ROM")
			item = (item >> 4) - 0x200
			if item > 0x2F then itemText = itemTable[item] else itemText = itemTableText[item] end
			doPrint = string.format("0x%06X - 0x%02X - %s -> %s", address, item, itemText, location)
			file:write("\n" .. doPrint)
		elseif locationType == 2 then
			item = memory.read_u8(address, "ROM")
			if item > 0x2F then itemText = itemTable[item] else itemText = itemTableText[item] end
			doPrint = string.format("0x%06X - 0x%02X - %s -> %s", address, item, itemText, location)
			file:write("\n" .. doPrint)
		elseif locationType == 3 then
			item = memory.read_u8(address, "ROM")
			if (item == 0x1D + 0x0E or item == 0x1E + 0x0E) then item = item - 0x0E
			elseif (item < 0x13) then item = item + 0x0A
			elseif (item >= 0x12 and item <= 0x18) then item = item + 0x0E end
			itemText = itemTable[item]
			doPrint = string.format("0x%06X - 0x%02X - %s -> %s", address, item, itemText, location)
			file:write("\n" .. doPrint)
		else
			item = memory.read_u8(address, "ROM")
			itemText = itemTable[item]
			doPrint = string.format("0x%06X - 0x%02X - %s -> %s", address, item, itemText, location)
			file:write("\n" .. doPrint)
		end
		
		i = i + 1
	end
	file:close()
	print("Success!!!")
end

